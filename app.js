// Event Listeners Setup
function setupEventListeners() {
    console.log('Setting up event listeners...');
    
    // Language toggle
    if (languageToggle) {
        languageToggle.addEventListener('click', function(e) {
            e.preventDefault();
            console.log('Language toggle clicked');
            switchLanguage();
        });
        console.log('Language toggle event listener added');
    } else {
        console.error('Language toggle button not found');
    }
    
    // Search functionality
    if (searchInput) {
        const handleSearch = function(e) {
            searchTerm = normalizeText(e.target.value);
            filterAndRenderFruits();
        };
        
        searchInput.addEventListener('input', handleSearch);
        searchInput.addEventListener('keyup', handleSearch);
        searchInput.addEventListener('change', handleSearch);
        
        searchInput.addEventListener('keypress', function(e) {
            if (e.key === 'Enter') {
                searchTerm = normalizeText(e.target.value);
                filterAndRenderFruits();
            }
        });
        console.log('Search event listeners added');
    }
    
    // Filter buttons
    if (filterButtons) {
        filterButtons.forEach(btn => {
            btn.addEventListener('click', handleFilter);
        });
        console.log('Filter button event listeners added');
    }
    
    // Comparison buttons
    if (compareBtn) {
        compareBtn.addEventListener('click', function(e) {
            e.preventDefault();
            console.log('Compare button clicked');
            showComparisonModal();
        });
    }
    if (resetBtn) {
        resetBtn.addEventListener('click', function(e) {
            e.preventDefault();
            resetComparison();
        });
    }
    
    // Modal close buttons
    document.querySelectorAll('.modal-close, .modal-close-btn').forEach(element => {
        element.addEventListener('click', function(e) {
            e.preventDefault();
            closeModals();
        });
    });
    
    // Modal overlay close
    document.querySelectorAll('.modal-overlay').forEach(element => {
        element.addEventListener('click', function(e) {
            if (e.target === this) {
                closeModals();
            }
        });
    });
    
    // Search button
    const searchBtn = document.querySelector('.search-btn');
    if (searchBtn) {
        searchBtn.addEventListener('click', function(e) {
            e.preventDefault();
            if (searchInput) {
                searchTerm = normalizeText(searchInput.value);
                filterAndRenderFruits();
            }
        });
    }
    
    // Keyboard shortcuts
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            closeModals();
        }
        if (e.ctrlKey && e.key === 'f') {
            e.preventDefault();
            if (searchInput) {
                searchInput.focus();
            }
        }
    });
    
    console.log('All event listeners set up');
}

// Filter Handler
function handleFilter(e) {
    e.preventDefault();
    console.log('Filter clicked:', e.target.dataset.filter);
    
    if (filterButtons) {
        filterButtons.forEach(btn => btn.classList.remove('active'));
    }
    e.target.classList.add('active');
    
    const filterValue = e.target.dataset.filter;
    currentFilter = filterValue;
    filterAndRenderFruits();
}

