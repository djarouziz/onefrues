// Normalize text for better search (Arabic and English)
function normalizeText(text) {
    return text.toLowerCase()
        .replace(/ÿ£|ÿ•|ÿ¢/g, 'ÿß')
        .replace(/ÿ©/g, 'Ÿá')
        .replace(/Ÿä/g, 'Ÿâ')
        .replace(/\s+/g, ' ')
        .trim();
}

// Language switching functionality
